package br.com.myapplication.api.dto;

public class CopasDoMundoVencidas {

    private Double max;
    private Double pla;
    private Integer pos;

    public Double getMax() {
        return max;
    }

    public void setMax(Double max) {
        this.max = max;
    }

    public Double getPla() {
        return pla;
    }

    public void setPla(Double pla) {
        this.pla = pla;
    }

    public Integer getPos() {
        return pos;
    }

    public void setPos(Integer pos) {
        this.pos = pos;
    }

}
